// auto-director-compose v1.0.0 — 1-Click AI Movie Generator
// Orchestrates: AI plan → scene insert → parallel clip generation → optional voiceover/music
import { serve } from "https://deno.land/std@0.224.0/http/server.ts";
import { createClient } from "https://esm.sh/@supabase/supabase-js@2";

const corsHeaders = {
  "Access-Control-Allow-Origin": "*",
  "Access-Control-Allow-Headers": "authorization, x-client-info, apikey, content-type",
};

type Mood = 'cinematic' | 'hype' | 'calm' | 'corporate' | 'playful' | 'dramatic';
type EnginePref = 'auto' | 'premium' | 'budget';
type Stage = 'plan' | 'execute';

interface AutoDirectorRequest {
  stage: Stage;                 // 'plan' = preview only, 'execute' = create project + clips
  projectId?: string;           // required for stage=execute when re-using preview
  idea: string;
  mood: Mood;
  targetDurationSec: number;    // 15 | 30 | 60
  enginePreference?: EnginePref;
  language?: string;
  // execute-only:
  approvedScenes?: PlannedScene[];
  voicePreset?: string | null;
  musicMood?: string | null;
  title?: string;
}

interface PlannedScene {
  orderIndex: number;
  sceneType: string;
  durationSeconds: number;
  aiPrompt: string;
  textOverlay?: { text: string; position: 'top' | 'center' | 'bottom' } | null;
  recommendedEngine: string; // 'ai-hailuo' | 'ai-kling' | ...
}

interface PlanResult {
  scenes: PlannedScene[];
  estimatedCostEuros: number;
  rationale: string;
  brandContext?: BrandContext | null;
}

interface BrandContext {
  brandName: string | null;
  primaryColor: string | null;
  secondaryColor: string | null;
  mood: string | null;
  brandTone: string | null;
  brandValues: string[];
}

const ENGINE_BY_PREF: Record<EnginePref, string[]> = {
  auto:    ['ai-hailuo', 'ai-seedance', 'ai-wan', 'ai-kling', 'ai-luma'],
  premium: ['ai-kling', 'ai-luma', 'ai-sora'],
  budget:  ['ai-wan', 'ai-seedance', 'ai-hailuo'],
};

// Cost per second per engine (standard quality, mirrors compose-video-clips)
const COST_PER_SEC: Record<string, number> = {
  'ai-hailuo': 0.15, 'ai-kling': 0.15, 'ai-sora': 0.25,
  'ai-wan': 0.10, 'ai-seedance': 0.12, 'ai-luma': 0.20,
};

serve(async (req) => {
  if (req.method === "OPTIONS") return new Response(null, { headers: corsHeaders });

  try {
    const authHeader = req.headers.get("Authorization");
    if (!authHeader) {
      return jsonResponse({ error: "UNAUTHORIZED", message: "Missing Authorization header" }, 401);
    }

    const supabaseClient = createClient(
      Deno.env.get("SUPABASE_URL") ?? "",
      Deno.env.get("SUPABASE_ANON_KEY") ?? ""
    );
    const token = authHeader.replace("Bearer ", "");
    const { data: { user }, error: authError } = await supabaseClient.auth.getUser(token);
    if (authError || !user) {
      return jsonResponse({ error: "UNAUTHORIZED" }, 401);
    }

    const supabaseAdmin = createClient(
      Deno.env.get("SUPABASE_URL") ?? "",
      Deno.env.get("SUPABASE_SERVICE_ROLE_KEY") ?? ""
    );

    const body: AutoDirectorRequest = await req.json();
    const stage = body.stage ?? 'plan';

    // Input validation
    if (!body.idea || body.idea.trim().length < 5) {
      return jsonResponse({ error: "INVALID_INPUT", message: "idea must be at least 5 characters" }, 400);
    }
    if (![15, 30, 60].includes(body.targetDurationSec)) {
      return jsonResponse({ error: "INVALID_INPUT", message: "targetDurationSec must be 15, 30 or 60" }, 400);
    }

    // ============== STAGE 1: PLAN ==============
    if (stage === 'plan') {
      const brandContext = await loadActiveBrandContext(supabaseAdmin, user.id);
      const plan = await generateScenePlan(body, brandContext);
      return jsonResponse({ ok: true, plan: { ...plan, brandContext } });
    }

    // ============== STAGE 2: EXECUTE ==============
    const scenes = body.approvedScenes;
    if (!scenes || scenes.length === 0) {
      return jsonResponse({ error: "MISSING_SCENES", message: "approvedScenes is required for execute stage" }, 400);
    }

    // Create new composer project
    const config = {
      idea: body.idea,
      mood: body.mood,
      targetDurationSec: body.targetDurationSec,
      sceneCount: scenes.length,
      enginePreference: body.enginePreference ?? 'auto',
      voicePreset: body.voicePreset ?? null,
      musicMood: body.musicMood ?? null,
      generatedAt: new Date().toISOString(),
    };

    const totalEstimatedCost = scenes.reduce(
      (sum, s) => sum + (COST_PER_SEC[s.recommendedEngine] ?? 0.15) * s.durationSeconds,
      0
    );

    const { data: project, error: projErr } = await supabaseAdmin
      .from('composer_projects')
      .insert({
        user_id: user.id,
        title: body.title || `Auto: ${body.idea.slice(0, 50)}`,
        category: 'custom',
        briefing: { idea: body.idea, mood: body.mood, autoGenerated: true },
        status: 'generating',
        storyboard: [],
        assembly_config: {},
        total_cost_euros: 0,
        language: body.language ?? 'de',
        auto_director_config: config,
      })
      .select()
      .single();

    if (projErr || !project) {
      console.error('[auto-director] project insert failed:', projErr);
      return jsonResponse({ error: "DB_ERROR", message: projErr?.message ?? "Failed to create project" }, 500);
    }

    // Bulk insert scenes
    const sceneRows = scenes.map((s) => ({
      project_id: project.id,
      order_index: s.orderIndex,
      scene_type: s.sceneType,
      duration_seconds: s.durationSeconds,
      clip_source: s.recommendedEngine,
      ai_prompt: s.aiPrompt,
      clip_status: 'pending',
      text_overlay: s.textOverlay ?? {},
      transition_type: 'fade',
      transition_duration: 0.5,
      clip_quality: 'standard',
      cost_euros: (COST_PER_SEC[s.recommendedEngine] ?? 0.15) * s.durationSeconds,
    }));

    const { data: insertedScenes, error: scenesErr } = await supabaseAdmin
      .from('composer_scenes')
      .insert(sceneRows)
      .select();

    if (scenesErr) {
      // rollback project
      await supabaseAdmin.from('composer_projects').delete().eq('id', project.id);
      console.error('[auto-director] scenes insert failed:', scenesErr);
      return jsonResponse({ error: "DB_ERROR", message: scenesErr.message }, 500);
    }

    // Trigger compose-video-clips asynchronously (fire-and-forget — caller polls scene status)
    const clipsPayload = {
      projectId: project.id,
      scenes: (insertedScenes ?? []).map((s: any) => ({
        id: s.id,
        clipSource: s.clip_source,
        clipQuality: s.clip_quality,
        aiPrompt: s.ai_prompt,
        durationSeconds: Number(s.duration_seconds),
      })),
    };

    // Async invoke without awaiting result — clips run in background
    supabaseClient.functions.invoke('compose-video-clips', {
      body: clipsPayload,
      headers: { Authorization: authHeader },
    }).catch((err) => {
      console.error('[auto-director] async clips invoke error:', err);
    });

    return jsonResponse({
      ok: true,
      projectId: project.id,
      sceneCount: scenes.length,
      estimatedCostEuros: Number(totalEstimatedCost.toFixed(4)),
      message: 'Auto-Director project created. Clips are generating in the background.',
    });

  } catch (err) {
    const msg = err instanceof Error ? err.message : 'Unknown error';
    console.error('[auto-director] error:', msg);
    return jsonResponse({ error: "INTERNAL_ERROR", message: msg }, 500);
  }
});

