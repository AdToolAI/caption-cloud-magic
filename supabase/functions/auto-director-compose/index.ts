// AI PLAN GENERATION via Lovable AI Gateway (Gemini 2.5 Pro with structured tool calling)
// ============================================================================
async function generateScenePlan(req: AutoDirectorRequest): Promise<PlanResult> {
  const LOVABLE_API_KEY = Deno.env.get("LOVABLE_API_KEY");
  if (!LOVABLE_API_KEY) {
    throw new Error("LOVABLE_API_KEY is not configured");
  }

  const enginePref = req.enginePreference ?? 'auto';
  const allowedEngines = ENGINE_BY_PREF[enginePref];
  const targetSec = req.targetDurationSec;
  // Aim for 4-6 second scenes → derive scene count
  const idealSceneCount = Math.max(2, Math.min(8, Math.round(targetSec / 5)));

  const systemPrompt = `You are an expert AI video director. Your job is to transform a single idea into a compelling video storyboard.

RULES:
- Generate EXACTLY ${idealSceneCount} scenes that together total ~${targetSec} seconds.
- Each scene duration: 3-8 seconds (preferably 4-6).
- Each ai_prompt must be a vivid, cinematic visual description (camera angle, subject, action, lighting, mood) — NO dialogue, NO subtitles in the prompt.
- Keep prompts concise (15-30 words each), suitable for AI video generation models.
- Use the requested mood "${req.mood}" to drive style (e.g. cinematic = wide angles & dramatic lighting; hype = fast cuts & bold colors; calm = slow pans & soft tones; corporate = clean & professional; playful = bright & dynamic; dramatic = high contrast & tension).
- Choose recommended_engine ONLY from this list: ${allowedEngines.join(', ')}.
- Pick the best engine per scene: ai-kling/ai-luma for character close-ups & dialogue feel, ai-hailuo for fast action, ai-seedance for stylish motion, ai-wan for budget shots, ai-sora for hero moments.
- Scene types: hook, problem, solution, feature, proof, cta, intro, outro, transition.
- Output language for any text_overlay: ${req.language ?? 'de'}.
- Add a short text_overlay (3-6 words) ONLY on hook and cta scenes.`;

  const userPrompt = `Idea: "${req.idea}"
Mood: ${req.mood}
Total duration target: ${targetSec}s
Engine preference: ${enginePref}

Create the storyboard now.`;

  const response = await fetch("https://ai.gateway.lovable.dev/v1/chat/completions", {
    method: "POST",
    headers: {
      Authorization: `Bearer ${LOVABLE_API_KEY}`,
      "Content-Type": "application/json",
    },
    body: JSON.stringify({
      model: "google/gemini-2.5-pro",
      messages: [
        { role: "system", content: systemPrompt },
        { role: "user", content: userPrompt },
      ],
      tools: [{
        type: "function",
        function: {
          name: "create_storyboard",
          description: "Return the structured scene plan",
          parameters: {
            type: "object",
            properties: {
              rationale: { type: "string", description: "1-2 sentence summary of the creative direction" },
              scenes: {
                type: "array",
                minItems: 2,
                maxItems: 8,
                items: {
                  type: "object",
                  properties: {
                    order_index: { type: "integer" },
                    scene_type: {
                      type: "string",
                      enum: ['hook', 'problem', 'solution', 'feature', 'proof', 'cta', 'intro', 'outro', 'transition', 'custom'],
                    },
                    duration_seconds: { type: "number", minimum: 3, maximum: 8 },
                    ai_prompt: { type: "string" },
                    recommended_engine: { type: "string", enum: allowedEngines as unknown as string[] },
                    text_overlay: {
                      type: ["object", "null"],
                      properties: {
                        text: { type: "string" },
                        position: { type: "string", enum: ['top', 'center', 'bottom'] },
                      },
                    },
                  },
                  required: ["order_index", "scene_type", "duration_seconds", "ai_prompt", "recommended_engine"],
                  additionalProperties: false,
                },
              },
            },
            required: ["rationale", "scenes"],
            additionalProperties: false,
          },
        },
      }],
      tool_choice: { type: "function", function: { name: "create_storyboard" } },
    }),
  });

  if (!response.ok) {
    if (response.status === 429) throw new Error("AI rate limit reached, please try again in a moment.");
    if (response.status === 402) throw new Error("AI credits exhausted — please top up Lovable AI credits.");
    const errText = await response.text();
    throw new Error(`AI gateway error ${response.status}: ${errText.slice(0, 200)}`);
  }

  const data = await response.json();
  const toolCall = data.choices?.[0]?.message?.tool_calls?.[0];
  if (!toolCall?.function?.arguments) {
    throw new Error("AI did not return a structured plan");
  }

  let parsed: { rationale: string; scenes: any[] };
  try {
    parsed = JSON.parse(toolCall.function.arguments);
  } catch {
    throw new Error("Failed to parse AI plan output");
  }

  const scenes: PlannedScene[] = parsed.scenes.map((s: any, idx: number) => ({
    orderIndex: typeof s.order_index === 'number' ? s.order_index : idx,
    sceneType: s.scene_type,
    durationSeconds: Number(s.duration_seconds),
    aiPrompt: String(s.ai_prompt),
    recommendedEngine: allowedEngines.includes(s.recommended_engine) ? s.recommended_engine : allowedEngines[0],
    textOverlay: s.text_overlay && s.text_overlay.text ? {
      text: String(s.text_overlay.text),
      position: (['top','center','bottom'].includes(s.text_overlay.position) ? s.text_overlay.position : 'bottom') as 'top'|'center'|'bottom',
    } : null,
  }));

  const estimatedCostEuros = scenes.reduce(
    (sum, s) => sum + (COST_PER_SEC[s.recommendedEngine] ?? 0.15) * s.durationSeconds,
    0
  );

  return {
    scenes,
    estimatedCostEuros: Number(estimatedCostEuros.toFixed(4)),
    rationale: parsed.rationale ?? '',
  };
}

function jsonResponse(payload: unknown, status = 200): Response {
  return new Response(JSON.stringify(payload), {
    status,
    headers: { ...corsHeaders, "Content-Type": "application/json" },
  });
}
