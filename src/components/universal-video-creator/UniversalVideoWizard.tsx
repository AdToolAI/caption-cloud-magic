import { useState, useCallback, useEffect, useRef } from 'react';
import { motion, AnimatePresence } from 'framer-motion';
import { Check, FileText, Image, Play, Music, Download, Sparkles, Layout, MessageCircle, Loader2, Video, AlertTriangle, RefreshCw, ArrowLeft, RotateCcw, Palette, Film, Upload } from 'lucide-react';
import { cn } from '@/lib/utils';
import { useAuth } from '@/hooks/useAuth';
import { useTranslation } from '@/hooks/useTranslation';
import { useLocalizedVideoCategories, useLocalizedCategoryName } from '@/hooks/useLocalizedVideoCategories';
import { Button } from '@/components/ui/button';
import { CategorySelector } from './CategorySelector';
import { MoodPresetSelector, type MoodConfig } from './MoodPresetSelector';
import { FilmStyleSelector } from './FilmStyleSelector';
import { UniversalModeSelector, type UniversalGenerationMode } from './UniversalModeSelector';
import { UniversalVideoConsultant } from './UniversalVideoConsultant';
import { UniversalAutoGenerationProgress } from './UniversalAutoGenerationProgress';
import { UniversalPreviewPlayer } from './UniversalPreviewPlayer';
import { UniversalExportStep } from './UniversalExportStep';
import { MultiImageUpload } from '@/components/video/MultiImageUpload';
import { toast } from 'sonner';
import { VIDEO_CATEGORIES, type VideoCategory, type UniversalConsultationResult, type UniversalVideoStyle } from '@/types/universal-video-creator';
import { Dialog, DialogContent, DialogHeader, DialogTitle, DialogDescription, DialogFooter } from '@/components/ui/dialog';
import { getWizardDraft, saveWizardDraft, clearAllDrafts, hasMeaningfulDraft } from '@/lib/universal-video-draft';
import { useNavigate } from 'react-router-dom';
import { AlertDialog, AlertDialogAction, AlertDialogCancel, AlertDialogContent, AlertDialogDescription, AlertDialogFooter, AlertDialogHeader, AlertDialogTitle } from '@/components/ui/alert-dialog';
import { supabase } from '@/integrations/supabase/client';

export function UniversalVideoWizard() {
  const { user } = useAuth();
  const navigate = useNavigate();
  const { t, language } = useTranslation();
  const localizedCategories = useLocalizedVideoCategories();
  
  // Draft recovery dialog state
  const [showDraftDialog, setShowDraftDialog] = useState(false);
  const [draftChecked, setDraftChecked] = useState(false);
  
  const [selectedCategory, setSelectedCategory] = useState<VideoCategory | null>(null);
  const [generationMode, setGenerationMode] = useState<UniversalGenerationMode | null>(null);
  const [currentStep, setCurrentStep] = useState(0);
  const [isAutoGenerating, setIsAutoGenerating] = useState(false);
  const [consultationResult, setConsultationResult] = useState<UniversalConsultationResult | null>(null);
  const [autoGeneratedProject, setAutoGeneratedProject] = useState<any>(null);
  const activeProgressIdRef = useRef<string | null>(null);
  const generationStartedAtRef = useRef<string | null>(null);
  const [moodConfig, setMoodConfig] = useState<MoodConfig | null>(null);
  const [selectedVisualStyle, setSelectedVisualStyle] = useState<UniversalVideoStyle | null>(null);
  const [error, setError] = useState<string | null>(null);
  const [retryCount, setRetryCount] = useState(0);
  const [rateLimitRetryKey, setRateLimitRetryKey] = useState(0);
  const [showResetConfirm, setShowResetConfirm] = useState(false);
  
  // Product image upload state
  const [productImages, setProductImages] = useState<Array<{ id: string; url: string; file: File; uploading?: boolean; progress?: number }>>([]);
  const [uploadedProductImageUrls, setUploadedProductImageUrls] = useState<string[]>([]);
  const [isUploadingImages, setIsUploadingImages] = useState(false);

  // Check if category needs product images
  const needsProductImages = selectedCategory === 'product-ad';

  // Helper to get localized category name
  const getCategoryName = (cat: VideoCategory | null): string => {
    if (!cat) return '';
    return localizedCategories.find(c => c.category === cat)?.name || '';
  };

  // Steps for manual mode
  const STEPS_MANUAL = [
    { id: 'category', label: t('uvc.stepCategory'), icon: Video, description: t('uvc.stepCategoryDesc') },
    ...(needsProductImages ? [{ id: 'product-images', label: t('uvc.stepProductImages') || 'Produktbilder', icon: Upload, description: t('uvc.stepProductImagesDesc') || 'Lade mindestens 4 Produktbilder hoch' }] : []),
    { id: 'mood', label: t('uvc.stepMood'), icon: Palette, description: t('uvc.stepMoodDesc') },
    { id: 'visual-style', label: t('uvc.stepVisualStyle'), icon: Film, description: t('uvc.stepVisualStyleDesc') },
    { id: 'mode-select', label: t('uvc.stepMode'), icon: Sparkles, description: t('uvc.stepModeDesc') },
    { id: 'consultation', label: t('uvc.stepConsultation'), icon: MessageCircle, description: t('uvc.stepConsultationDesc') },
    { id: 'briefing', label: t('uvc.stepBriefing'), icon: FileText, description: t('uvc.stepBriefingDesc') },
    { id: 'script', label: t('uvc.stepScript'), icon: Sparkles, description: t('uvc.stepScriptDesc') },
    { id: 'storyboard', label: t('uvc.stepStoryboard'), icon: Layout, description: t('uvc.stepStoryboardDesc') },
    { id: 'visuals', label: t('uvc.stepVisuals'), icon: Image, description: t('uvc.stepVisualsDesc') },
    { id: 'animation', label: t('uvc.stepAnimation'), icon: Play, description: t('uvc.stepAnimationDesc') },
    { id: 'audio', label: t('uvc.stepAudio'), icon: Music, description: t('uvc.stepAudioDesc') },
    { id: 'export', label: t('uvc.stepExport'), icon: Download, description: t('uvc.stepExportDesc') },
  ];

  // Steps for full-service mode  
  const STEPS_FULL_SERVICE = [
    { id: 'category', label: t('uvc.stepCategory'), icon: Video, description: t('uvc.stepCategoryDesc') },
    ...(needsProductImages ? [{ id: 'product-images', label: t('uvc.stepProductImages') || 'Produktbilder', icon: Upload, description: t('uvc.stepProductImagesDesc') || 'Lade mindestens 4 Produktbilder hoch' }] : []),
    { id: 'mood', label: t('uvc.stepMood'), icon: Palette, description: t('uvc.stepMoodDesc') },
    { id: 'visual-style', label: t('uvc.stepVisualStyle'), icon: Film, description: t('uvc.stepVisualStyleDesc') },
    { id: 'mode-select', label: t('uvc.stepMode'), icon: Sparkles, description: t('uvc.stepModeDesc') },
    { id: 'consultation', label: t('uvc.stepConsultation'), icon: MessageCircle, description: t('uvc.stepConsultationDescFull') },
    { id: 'generating', label: t('uvc.stepGenerating'), icon: Loader2, description: t('uvc.stepGeneratingDesc') },
    { id: 'preview', label: t('uvc.stepPreview'), icon: Play, description: t('uvc.stepPreviewDesc') },
    { id: 'export', label: t('uvc.stepDownload'), icon: Download, description: t('uvc.stepDownloadDesc') },
  ];

  // Validate consultation result has minimum required data
  const validateConsultationResult = (result: UniversalConsultationResult): { valid: boolean; errors: string[] } => {
    const errors: string[] = [];
    if (!result.category) errors.push(t('uvc.valCategoryMissing'));
    // Storytelling doesn't need product info — only ad categories do
    const isStoryCategory = result.category === 'storytelling';
    if (!isStoryCategory && !result.productDescription && !result.productName) errors.push(t('uvc.valProductMissing'));
    if (!result.storytellingStructure) errors.push(t('uvc.valStructureMissing'));
    return { valid: errors.length === 0, errors };
  };

  // beforeunload warning during active generation
  useEffect(() => {
    if (!isAutoGenerating) return;
    const handler = (e: BeforeUnloadEvent) => {
      e.preventDefault();
      e.returnValue = '';
    };
    window.addEventListener('beforeunload', handler);
    return () => window.removeEventListener('beforeunload', handler);
  }, [isAutoGenerating]);

  // Check for draft on mount — show dialog if meaningful draft exists
  useEffect(() => {
    if (draftChecked) return;
    setDraftChecked(true);
    if (hasMeaningfulDraft()) {
      setShowDraftDialog(true);
    }
  }, [draftChecked]);

  const handleResumeDraft = () => {
    const draft = getWizardDraft();
    if (draft) {
      setSelectedCategory(draft.category as VideoCategory | null);
      setGenerationMode(draft.mode as UniversalGenerationMode | null);
      setCurrentStep(draft.step || 0);
      setMoodConfig(draft.moodConfig || null);
      setConsultationResult(draft.consultationResult || null);
      setAutoGeneratedProject(draft.autoGeneratedProject || null);
    }
    setShowDraftDialog(false);
  };

  const handleStartFresh = () => {
    clearAllDrafts();
    setShowDraftDialog(false);
  };

  // Persist wizard state to localStorage
  useEffect(() => {
    if (selectedCategory || generationMode || currentStep > 0) {
      saveWizardDraft({
        category: selectedCategory,
        mode: generationMode,
        step: currentStep,
        moodConfig,
        consultationResult,
        autoGeneratedProject,
      });
    }
  }, [selectedCategory, generationMode, currentStep, moodConfig, consultationResult, autoGeneratedProject]);

  // r54b: Fallback — recover from DB when on generating (done) or preview step without project
  const dbFallbackAttempted = useRef(false);
  useEffect(() => {
    const isStuckOnGenerating = currentStepId === 'generating' && !isAutoGenerating && !autoGeneratedProject;
    const isPreviewWithoutProject = currentStepId === 'preview' && !autoGeneratedProject;
    
    if ((isStuckOnGenerating || isPreviewWithoutProject) && user && !dbFallbackAttempted.current) {
      dbFallbackAttempted.current = true;
      console.log('[UniversalVideoWizard] Phase12 DB recovery triggered, stepId=', currentStepId);
      import('@/integrations/supabase/client').then(({ supabase }) => {
        let query = supabase
          .from('universal_video_progress')
          .select('result_data')
          .eq('user_id', user.id)
          .eq('status', 'completed')
          .order('updated_at', { ascending: false })
          .limit(1);
        
        if (generationStartedAtRef.current) {
          query = query.gte('created_at', generationStartedAtRef.current);
        }
        
        query.single()
          .then(({ data }) => {
            if (data?.result_data) {
              const rd = data.result_data as any;
              const recovered = {
                outputUrl: rd.outputUrl || rd.output_url,
                renderId: rd.renderId || rd.render_id,
                ...rd,
              };
              if (recovered.outputUrl) {
                console.log('[UniversalVideoWizard] Phase12: Recovered project from DB:', recovered.outputUrl);
                setAutoGeneratedProject(recovered);
                if (currentStepId === 'generating') {
                  const previewIdx = STEPS.findIndex(s => s.id === 'preview');
                  if (previewIdx !== -1) setCurrentStep(previewIdx);
                }
              }
            }
          });
      });
    }
  }, [currentStep, autoGeneratedProject, user, isAutoGenerating]);

  // Select steps based on mode
  const STEPS = generationMode === 'full-service' ? STEPS_FULL_SERVICE : 
                generationMode === 'manual' ? STEPS_MANUAL :
                [
                  { id: 'category', label: t('uvc.stepCategory'), icon: Video, description: t('uvc.stepCategoryDesc') },
                  ...(needsProductImages ? [{ id: 'product-images', label: t('uvc.stepProductImages') || 'Produktbilder', icon: Upload, description: t('uvc.stepProductImagesDesc') || 'Lade mindestens 4 Produktbilder hoch' }] : []),
                  { id: 'mood', label: t('uvc.stepMood'), icon: Palette, description: t('uvc.stepMoodDesc') },
                  { id: 'visual-style', label: t('uvc.stepVisualStyle'), icon: Film, description: t('uvc.stepVisualStyleDesc') },
                  { id: 'mode-select', label: t('uvc.stepMode'), icon: Sparkles, description: t('uvc.stepModeDesc') }
                ];

  const currentStepId = STEPS[currentStep]?.id;

  const handleCategorySelect = (category: VideoCategory) => {
    setSelectedCategory(category);
    setError(null);
  };

  const handleCategoryConfirm = () => {
    if (selectedCategory) {
      // If product category, go to image upload step; otherwise skip to mood
      setCurrentStep(1);
    }
  };

  // Upload product images to Supabase storage
  const handleUploadProductImages = async () => {
    if (!user || productImages.length < 4) {
      toast.error(language === 'de' ? 'Mindestens 4 Produktbilder erforderlich' : 'At least 4 product images required');
      return;
    }
    
    setIsUploadingImages(true);
    const urls: string[] = [];
    
    try {
      for (const img of productImages) {
        const fileExt = img.file.name.split('.').pop();
        const fileName = `${user.id}/product-images/${Date.now()}-${Math.random().toString(36).substring(7)}.${fileExt}`;
        
        const { data, error: uploadError } = await supabase.storage
          .from('media-assets')
          .upload(fileName, img.file, { cacheControl: '3600', upsert: false });
        
        if (uploadError) throw uploadError;
        
        const { data: { publicUrl } } = supabase.storage
          .from('media-assets')
          .getPublicUrl(data.path);
        
        urls.push(publicUrl);
      }
      
      setUploadedProductImageUrls(urls);
      toast.success(language === 'de' ? `${urls.length} Bilder hochgeladen` : `${urls.length} images uploaded`);
      
      // Find the index of the next step after product-images
      const currentSteps = generationMode === 'full-service' ? STEPS_FULL_SERVICE : 
                          generationMode === 'manual' ? STEPS_MANUAL :
                          STEPS;
      const currentIdx = currentSteps.findIndex(s => s.id === 'product-images');
      if (currentIdx !== -1) {
        setCurrentStep(currentIdx + 1);
      }
    } catch (err: any) {
      console.error('Product image upload error:', err);
      toast.error(language === 'de' ? 'Fehler beim Hochladen' : 'Upload failed');
    } finally {
      setIsUploadingImages(false);
    }
  };

  const handleMoodConfirm = (config: MoodConfig) => {
    setMoodConfig(config);
    // Find next step after mood
    const currentSteps = generationMode === 'full-service' ? STEPS_FULL_SERVICE : 
                        generationMode === 'manual' ? STEPS_MANUAL : STEPS;
    const moodIdx = currentSteps.findIndex(s => s.id === 'mood');
    setCurrentStep(moodIdx + 1);
  };

  const handleVisualStyleConfirm = (style: UniversalVideoStyle) => {
    setSelectedVisualStyle(style);
    const currentSteps = generationMode === 'full-service' ? STEPS_FULL_SERVICE : 
                        generationMode === 'manual' ? STEPS_MANUAL : STEPS;
    const vsIdx = currentSteps.findIndex(s => s.id === 'visual-style');
    setCurrentStep(vsIdx + 1);
  };

  const handleModeSelect = (mode: UniversalGenerationMode) => {
    setGenerationMode(mode);
    // After mode selection the steps array changes, find consultation step
    const nextSteps = mode === 'full-service' ? STEPS_FULL_SERVICE : STEPS_MANUAL;
    const consultIdx = nextSteps.findIndex(s => s.id === 'consultation');
    setCurrentStep(consultIdx);
  };

  const handleBackToCategory = () => {
    setSelectedCategory(null);
    setGenerationMode(null);
    setCurrentStep(0);
    setError(null);
  };

  const handleResetWizard = () => {
    if (isAutoGenerating) {
      setShowResetConfirm(true);
      return;
    }
    performReset();
  };

  const performReset = () => {
    if (isAutoGenerating) {
      toast.info(t('uvc.toastBackgroundGeneration'), { duration: 8000 });
    }
    setSelectedCategory(null);
    setGenerationMode(null);
    setMoodConfig(null);
    setSelectedVisualStyle(null);
    setCurrentStep(0);
    setError(null);
    setIsAutoGenerating(false);
    setAutoGeneratedProject(null);
    setConsultationResult(null);
    clearAllDrafts();
    setShowResetConfirm(false);
  };

  const handleMinimizeToBackground = () => {
    toast.info(t('uvc.toastBackgroundGeneration'), { duration: 8000 });
    navigate('/sora-ai-videos');
  };

  const handleConsultationComplete = (result: UniversalConsultationResult) => {
    const validation = validateConsultationResult(result);
    if (!validation.valid) {
      console.warn('[UniversalVideoWizard] Consultation result validation failed:', validation.errors);
      result.storytellingStructure = result.storytellingStructure || 'problem-solution';
      result.productDescription = result.productDescription || result.productName || 'Video-Projekt';
    }

    if (moodConfig) {
      result.moodConfig = moodConfig;
    }

    // Inject uploaded product images
    if (uploadedProductImageUrls.length > 0) {
      result.productImages = uploadedProductImageUrls;
    }

    if (selectedVisualStyle) {
      result.visualStyle = selectedVisualStyle;
      console.log('[Wizard] Injected selectedVisualStyle into consultation result:', selectedVisualStyle);
    }
    console.log('[Wizard] Final visualStyle for generation:', result.visualStyle, '| selectedVisualStyle:', selectedVisualStyle);

    setConsultationResult(result);
    setError(null);
    
    if (generationMode === 'full-service') {
      setAutoGeneratedProject(null);
      activeProgressIdRef.current = null;
      generationStartedAtRef.current = new Date().toISOString();
      dbFallbackAttempted.current = false;
      
      setIsAutoGenerating(true);
      // Find the generating step
      const genIdx = STEPS_FULL_SERVICE.findIndex(s => s.id === 'generating');
      setCurrentStep(genIdx);
      toast.info(t('uvc.toastVideoStarted'));
    } else {
      // Manual mode next step after consultation
      const nextIdx = STEPS_MANUAL.findIndex(s => s.id === 'consultation') + 1;
      setCurrentStep(nextIdx);
    }
  };

  const handleConsultationSkip = () => {
    if (generationMode === 'full-service') {
      toast.error(t('uvc.toastConsultationRequired'));
      return;
    }
    const defaultResult: UniversalConsultationResult = {
      category: selectedCategory!,
      projectName: `${getCategoryName(selectedCategory)}-Projekt`,
      completedAt: new Date().toISOString(),
      companyName: '',
      productName: '',
      productDescription: '',
      targetAudience: '',
      coreProblem: '',
      solution: '',
      uniqueSellingPoints: [],
      storytellingStructure: 'problem-solution',
      emotionalTone: 'professional',
      keyMessage: '',
      desiredAction: '',
      ctaText: '',
      visualStyle: selectedVisualStyle || 'flat-design',
      brandColors: [],
      hasCharacter: false,
      voiceGender: 'male',
      voiceLanguage: language === 'en' ? 'en' : language === 'es' ? 'es' : 'de',
      voiceTone: 'professionell',
      musicStyle: 'corporate',
      musicMood: 'inspirational',
      videoDuration: 60,
      aspectRatio: '16:9',
      outputFormats: ['16:9'],
      categorySpecific: {},
      briefingSummary: '',
    };
    setConsultationResult(defaultResult);
    setCurrentStep(4);
  };

  const handleAutoGenerationComplete = (project: any) => {
    if (!project || !project.outputUrl) {
      console.warn('[UniversalVideoWizard] Phase12: onComplete called without valid project, staying in error state');
      setError(t('uvc.generationFailedDesc'));
      setIsAutoGenerating(false);
      return;
    }
    
    setAutoGeneratedProject(project);
    setIsAutoGenerating(false);
    setCurrentStep(6);
    toast.success(t('uvc.toastVideoCreated'));
  };

  const handleAutoGenerationError = (errorMessage: string) => {
    setError(errorMessage);
    setIsAutoGenerating(false);
  };

  const handleSwitchToManual = (partialProject: any) => {
    setGenerationMode('manual');
    setIsAutoGenerating(false);
    setAutoGeneratedProject(partialProject);
    setError(null);
    
    if (partialProject?.script) {
      setCurrentStep(6);
    } else {
      setCurrentStep(4);
    }
    toast.info(t('uvc.toastSwitchedToManual'));
  };

  const MAX_AUTO_RETRIES = 9;
  const getDiagnosticProfile = (retry: number): string => {
    const profiles = ['A', 'B', 'C', 'D', 'E', 'F', 'G', 'H', 'I', 'J'];
    return profiles[Math.min(retry, profiles.length - 1)];
  };

  const handleRetry = () => {
    console.log('[UniversalVideoWizard] handleRetry called, current isAutoGenerating:', isAutoGenerating);
    setError(null);
    setIsAutoGenerating(false);
    setAutoGeneratedProject(null);
    generationStartedAtRef.current = new Date().toISOString();
    dbFallbackAttempted.current = false;
    
    const newRetryCount = retryCount + 1;
    
    if (newRetryCount > MAX_AUTO_RETRIES) {
      console.log(`[UniversalVideoWizard] Max retries (${MAX_AUTO_RETRIES}) reached, showing final error`);
      setError(t('uvc.toastMaxRetries'));
      return;
    }
    
    setRetryCount(newRetryCount);
    
    const diagProfile = getDiagnosticProfile(newRetryCount);
    console.log(`[UniversalVideoWizard] Retry #${newRetryCount}/${MAX_AUTO_RETRIES}, diagnosticProfile=${diagProfile}`);
    toast.info(t('uvc.toastDiagProfile', { profile: diagProfile, count: String(newRetryCount), max: String(MAX_AUTO_RETRIES) }));
    
    setTimeout(() => {
      if (consultationResult && generationMode === 'full-service') {
        console.log('[UniversalVideoWizard] Re-mounting with isAutoGenerating=true, profile:', diagProfile);
        setIsAutoGenerating(true);
        setCurrentStep(5);
      }
    }, 150);
  };

  const handleRateLimitRetry = () => {
    const diagProfile = getDiagnosticProfile(retryCount);
    console.log(`[UniversalVideoWizard] Rate-limit retry with SAME profile ${diagProfile} (retryCount stays ${retryCount})`);
    setError(null);
    setIsAutoGenerating(false);
    setRateLimitRetryKey(prev => prev + 1);
    setTimeout(() => {
      if (consultationResult && generationMode === 'full-service') {
        setIsAutoGenerating(true);
        setCurrentStep(5);
      }
    }, 150);
  };

  const handleBackToConsultation = () => {
    const consultIdx = STEPS.findIndex(s => s.id === 'consultation');
    setCurrentStep(consultIdx >= 0 ? consultIdx : 4);
    setIsAutoGenerating(false);
    setError(null);
  };

  const handleBack = () => {
    if (currentStep > 0) {
      setCurrentStep(currentStep - 1);
      setError(null);
    }
  };

  const categoryName = getCategoryName(selectedCategory);

  // Auth check
  if (!user) {
    return (
      <div className="max-w-xl mx-auto text-center py-12">
        <div className="w-20 h-20 rounded-full bg-muted/20 flex items-center justify-center mx-auto mb-6 border border-white/10">
          <AlertTriangle className="h-10 w-10 text-amber-500" />
        </div>
        <h2 className="text-2xl font-bold mb-4">{t('uvc.loginRequired')}</h2>
        <p className="text-muted-foreground mb-6">
          {t('uvc.loginRequiredDesc')}
        </p>
      </div>
    );
  }

  return (
    <div className="max-w-7xl mx-auto">
      {/* Draft Recovery Dialog */}
      <Dialog open={showDraftDialog} onOpenChange={setShowDraftDialog}>
        <DialogContent className="sm:max-w-md">
          <DialogHeader>
            <DialogTitle className="flex items-center gap-2">
              <FileText className="h-5 w-5 text-[#F5C76A]" />
              {t('uvc.draftFound')}
            </DialogTitle>
            <DialogDescription>
              {t('uvc.draftFoundDesc')}
            </DialogDescription>
          </DialogHeader>
          <DialogFooter className="flex gap-2 sm:gap-2">
            <Button variant="outline" onClick={handleStartFresh}>
              <RotateCcw className="h-4 w-4 mr-2" />
              {t('uvc.startFresh')}
            </Button>
            <Button onClick={handleResumeDraft} className="bg-gradient-to-r from-[#F5C76A] to-amber-500 text-black hover:shadow-[0_0_20px_rgba(245,199,106,0.4)]">
              <Play className="h-4 w-4 mr-2" />
              {t('uvc.continueBtn')}
            </Button>
          </DialogFooter>
        </DialogContent>
      </Dialog>

      {/* Reset Confirmation Dialog */}
      <AlertDialog open={showResetConfirm} onOpenChange={setShowResetConfirm}>
        <AlertDialogContent>
          <AlertDialogHeader>
            <AlertDialogTitle>{t('uvc.videoStillGenerating')}</AlertDialogTitle>
            <AlertDialogDescription>
              {t('uvc.videoStillGeneratingDesc')}
            </AlertDialogDescription>
          </AlertDialogHeader>
          <AlertDialogFooter>
            <AlertDialogCancel>{t('uvc.cancel')}</AlertDialogCancel>
            <AlertDialogAction onClick={performReset}>
              {t('uvc.startNewVideoAnyway')}
            </AlertDialogAction>
          </AlertDialogFooter>
        </AlertDialogContent>
      </AlertDialog>
      {error && (
        <motion.div
          initial={{ opacity: 0, y: -20 }}
          animate={{ opacity: 1, y: 0 }}
          className="mb-6 p-4 bg-destructive/10 border border-destructive/30 rounded-xl flex items-center justify-between"
        >
          <div className="flex items-center gap-3">
            <AlertTriangle className="h-5 w-5 text-destructive" />
            <span className="text-sm">{error}</span>
          </div>
          <div className="flex gap-2">
            <Button variant="ghost" size="sm" onClick={() => setError(null)}>
              {t('uvc.close')}
            </Button>
            <Button variant="outline" size="sm" onClick={handleBackToConsultation} className="border-muted">
              <ArrowLeft className="h-4 w-4 mr-2" />
              {t('uvc.backToInterview')}
            </Button>
            <Button variant="outline" size="sm" onClick={handleRetry} className="border-destructive/30">
              <RefreshCw className="h-4 w-4 mr-2" />
              {t('uvc.retryBtn')}
            </Button>
          </div>
        </motion.div>
      )}

      {/* Back Button + Reset Button - when in wizard */}
      {currentStep > 0 && (
        <motion.div
          initial={{ opacity: 0 }}
          animate={{ opacity: 1 }}
          className="mb-4 flex items-center gap-2"
        >
          {currentStepId !== 'generating' && (
            <Button variant="ghost" size="sm" onClick={handleBack} className="text-muted-foreground hover:text-foreground">
              <ArrowLeft className="h-4 w-4 mr-2" />
              {t('uvc.back')}
            </Button>
          )}
          <Button variant="outline" size="sm" onClick={handleResetWizard}>
            <RotateCcw className="h-4 w-4 mr-2" />
            {t('uvc.startNewVideo')}
          </Button>
        </motion.div>
      )}

      {/* Phase Stepper - compact dots grouped by phase */}
      {selectedCategory && generationMode && (
        <div className="mb-8">
          {(() => {
            // Define phase groups based on mode
            const phases = generationMode === 'full-service' 
              ? [
                  { label: t('uvc.stepPhasePreparation'), stepIds: ['category', 'product-images', 'mood', 'visual-style', 'mode-select'] },
                  { label: t('uvc.stepPhaseConsultation'), stepIds: ['consultation'] },
                  { label: t('uvc.stepPhaseGeneration'), stepIds: ['generating'] },
                  { label: t('uvc.stepPhasePreview'), stepIds: ['preview'] },
                  { label: t('uvc.stepPhaseExport'), stepIds: ['export'] },
                ]
              : [
                  { label: t('uvc.stepPhasePreparation'), stepIds: ['category', 'product-images', 'mood', 'visual-style', 'mode-select'] },
                  { label: t('uvc.stepPhaseConsultation'), stepIds: ['consultation'] },
                  { label: t('uvc.stepPhaseConcept'), stepIds: ['briefing', 'script', 'storyboard'] },
                  { label: t('uvc.stepPhaseProduction'), stepIds: ['visuals', 'animation'] },
                  { label: t('uvc.stepPhaseAudio'), stepIds: ['audio'] },
                  { label: t('uvc.stepPhaseExport'), stepIds: ['export'] },
                ];

            // Filter phases to only include step IDs that actually exist in STEPS
            const existingStepIds = new Set(STEPS.map(s => s.id));
            const filteredPhases = phases
              .map(p => ({ ...p, stepIds: p.stepIds.filter(id => existingStepIds.has(id)) }))
              .filter(p => p.stepIds.length > 0);

            return (
              <div className="flex items-center justify-center gap-3 sm:gap-4">
                {filteredPhases.map((phase, phaseIdx) => {
                  const phaseStepIndices = phase.stepIds.map(id => STEPS.findIndex(s => s.id === id)).filter(i => i !== -1);
                  const minIdx = Math.min(...phaseStepIndices);
                  const maxIdx = Math.max(...phaseStepIndices);
                  const isActive = currentStep >= minIdx && currentStep <= maxIdx;
                  const isCompleted = currentStep > maxIdx;

                  return (
                    <div key={phaseIdx} className="flex items-center">
                      <motion.button
                        onClick={() => isCompleted && setCurrentStep(minIdx)}
                        disabled={!isCompleted}
                        className="flex flex-col items-center gap-1.5 group"
                        whileHover={isCompleted ? { scale: 1.05 } : {}}
                        whileTap={isCompleted ? { scale: 0.95 } : {}}
                      >
                        <div className={cn(
                          'w-9 h-9 sm:w-10 sm:h-10 rounded-full flex items-center justify-center transition-all duration-300 border-2',
                          isActive && 'bg-[#F5C76A] border-[#F5C76A] text-black shadow-[0_0_20px_rgba(245,199,106,0.4)]',
                          isCompleted && 'bg-[#F5C76A]/20 border-[#F5C76A] text-[#F5C76A] cursor-pointer hover:bg-[#F5C76A]/30',
                          !isActive && !isCompleted && 'bg-muted/20 border-white/10 text-muted-foreground'
                        )}>
                          {isCompleted ? (
                            <Check className="h-4 w-4" />
                          ) : (
                            <span className="text-xs font-bold">{phaseIdx + 1}</span>
                          )}
                        </div>
                        <span className={cn(
                          'text-[10px] sm:text-xs font-medium max-w-[70px] text-center leading-tight',
                          isActive && 'text-[#F5C76A]',
                          isCompleted && 'text-[#F5C76A]/70',
                          !isActive && !isCompleted && 'text-muted-foreground'
                        )}>
                          {phase.label}
                        </span>
                      </motion.button>
                      {phaseIdx < filteredPhases.length - 1 && (
                        <div className={cn(
                          'h-px w-6 sm:w-10 ml-3 sm:ml-4',
                          isCompleted ? 'bg-[#F5C76A]' : 'bg-border'
                        )} />
                      )}
                    </div>
                  );
                })}
              </div>
            );
          })()}
        </div>
      )}

      {/* Step Content */}
      <AnimatePresence mode="wait">
        <motion.div
          key={currentStepId}
          initial={{ opacity: 0, y: 20 }}
          animate={{ opacity: 1, y: 0 }}
          exit={{ opacity: 0, y: -20 }}
          transition={{ duration: 0.3 }}
        >
          {/* Category Selection */}
          {currentStepId === 'category' && (
            <div className="space-y-6">
              <CategorySelector
                selectedCategory={selectedCategory}
                onSelectCategory={handleCategorySelect}
              />
              {selectedCategory && (
                <motion.div
                  initial={{ opacity: 0, y: 20 }}
                  animate={{ opacity: 1, y: 0 }}
                  className="flex justify-center"
                >
                  <button
                    onClick={handleCategoryConfirm}
                    className={cn(
                      "px-8 py-3 rounded-xl font-semibold text-black",
                      "bg-gradient-to-r from-[#F5C76A] to-amber-500",
                      "hover:shadow-[0_0_30px_rgba(245,199,106,0.4)]",
                      "transition-all duration-300"
                    )}
                  >
                    {t('uvc.continueWith')} {categoryName}
                  </button>
                </motion.div>
              )}
            </div>
          )}

          {/* Product Images Upload Step */}
          {currentStepId === 'product-images' && selectedCategory && (
            <div className="max-w-2xl mx-auto space-y-6">
              <div className="text-center mb-6">
                <h2 className="text-2xl font-bold mb-2 text-[#F5C76A]">
                  {language === 'de' ? 'Produktbilder hochladen' : language === 'es' ? 'Subir imágenes del producto' : 'Upload Product Images'}
                </h2>
                <p className="text-muted-foreground">
                  {language === 'de' 
                    ? 'Lade mindestens 4 Fotos deines Produkts hoch. Diese werden per KI in die passenden Szenen eingebettet.'
                    : language === 'es'
                    ? 'Sube al menos 4 fotos de tu producto. Serán integradas en las escenas por IA.'
                    : 'Upload at least 4 photos of your product. They will be AI-enhanced to fit each scene.'}
                </p>
              </div>
              <MultiImageUpload
                value={productImages}
                onChange={setProductImages}
                minFiles={4}
                maxFiles={10}
                label={language === 'de' ? 'Produktbilder' : language === 'es' ? 'Imágenes del producto' : 'Product Images'}
              />
              <div className="flex justify-center">
                <Button
                  onClick={handleUploadProductImages}
                  disabled={productImages.length < 4 || isUploadingImages}
                  className="px-8 py-3 rounded-xl font-semibold text-black bg-gradient-to-r from-[#F5C76A] to-amber-500 hover:shadow-[0_0_30px_rgba(245,199,106,0.4)] transition-all duration-300 disabled:opacity-50"
                >
                  {isUploadingImages 
                    ? (language === 'de' ? 'Wird hochgeladen...' : 'Uploading...') 
                    : (language === 'de' ? `Weiter mit ${productImages.length} Bildern` : `Continue with ${productImages.length} images`)}
                </Button>
              </div>
            </div>
          )}


          {currentStepId === 'mood' && selectedCategory && (
            <MoodPresetSelector onConfirm={handleMoodConfirm} />
          )}

          {/* Visual Style Selection */}
          {currentStepId === 'visual-style' && selectedCategory && (
            <FilmStyleSelector onConfirm={handleVisualStyleConfirm} />
          )}

          {/* Mode Selection */}
          {currentStepId === 'mode-select' && selectedCategory && (
            <UniversalModeSelector
              selectedCategory={selectedCategory}
              onSelectMode={handleModeSelect}
              onBack={handleBackToCategory}
            />
          )}

          {/* Consultation */}
          {currentStepId === 'consultation' && selectedCategory && generationMode && (
            <UniversalVideoConsultant
              category={selectedCategory}
              mode={generationMode}
              onConsultationComplete={handleConsultationComplete}
              onSkip={handleConsultationSkip}
            />
          )}

          {/* Auto Generation (Full-Service) */}
          {currentStepId === 'generating' && isAutoGenerating && consultationResult && selectedCategory && user && (
            <UniversalAutoGenerationProgress
              key={`gen-${retryCount}-rl-${rateLimitRetryKey}`}
              consultationResult={consultationResult}
              category={selectedCategory}
              userId={user.id}
              onComplete={handleAutoGenerationComplete}
              onSwitchToManual={handleSwitchToManual}
              onRetry={handleRetry}
              onRateLimitRetry={handleRateLimitRetry}
              onMinimize={handleMinimizeToBackground}
              diagnosticProfile={getDiagnosticProfile(retryCount)}
            />
          )}

          {/* Recovery UI when generating failed */}
          {currentStepId === 'generating' && !isAutoGenerating && !autoGeneratedProject && (
            <div className="max-w-lg mx-auto text-center py-12 space-y-6">
              {!dbFallbackAttempted.current ? (
                <>
                  <div className="w-20 h-20 rounded-full bg-muted/30 flex items-center justify-center mx-auto border border-border animate-pulse">
                    <RefreshCw className="h-10 w-10 text-muted-foreground animate-spin" />
                  </div>
                  <h2 className="text-2xl font-bold">{t('uvc.projectLoading')}</h2>
                  <p className="text-muted-foreground">{t('uvc.projectLoadingDesc')}</p>
                </>
              ) : (
                <>
                  <div className="w-20 h-20 rounded-full bg-destructive/10 flex items-center justify-center mx-auto border border-destructive/30">
                    <AlertTriangle className="h-10 w-10 text-destructive" />
                  </div>
                  <h2 className="text-2xl font-bold">{t('uvc.generationFailed')}</h2>
                  <p className="text-muted-foreground">
                    {error || t('uvc.generationFailedDesc')}
                  </p>
                  <div className="flex gap-4 justify-center">
                    <Button variant="outline" onClick={handleBackToConsultation}>
                      <ArrowLeft className="h-4 w-4 mr-2" />
                      {t('uvc.backToInterview')}
                    </Button>
                    <Button onClick={handleRetry}>
                      <RefreshCw className="h-4 w-4 mr-2" />
                      {t('uvc.retryBtn')}
                    </Button>
                  </div>
                </>
              )}
            </div>
          )}

          {/* Preview */}
          {currentStepId === 'preview' && autoGeneratedProject && (
            <div className="space-y-6">
              <div className="text-center mb-6">
                <h2 className="text-2xl font-bold mb-2 text-[#F5C76A]">{t('uvc.videoPreview')}</h2>
                <p className="text-muted-foreground">
                  {t('uvc.videoCreatedSuccess', { name: categoryName })}
                </p>
              </div>
              <UniversalPreviewPlayer
                project={autoGeneratedProject}
                onExport={() => setCurrentStep(STEPS.length - 1)}
                showExportButton={true}
              />
            </div>
          )}

          {/* Export Step */}
          {currentStepId === 'export' && autoGeneratedProject && selectedCategory && user && (
            <UniversalExportStep
              project={autoGeneratedProject}
              category={selectedCategory}
              userId={user.id}
              onBack={() => setCurrentStep(currentStep - 1)}
              onComplete={(renders) => {
                toast.success(t('uvc.toastExportSuccess', { count: String(renders.length) }));
              }}
            />
          )}

          {/* Manual Mode Steps - Placeholder */}
          {generationMode === 'manual' && ['briefing', 'script', 'storyboard', 'visuals', 'animation', 'audio', 'export'].includes(currentStepId || '') && (
            <div className="text-center py-12">
              <div className="w-20 h-20 rounded-full bg-muted/20 flex items-center justify-center mx-auto mb-6">
                <FileText className="h-10 w-10 text-muted-foreground" />
              </div>
              <h2 className="text-2xl font-bold mb-4">
                {STEPS.find(s => s.id === currentStepId)?.label}
              </h2>
              <p className="text-muted-foreground mb-6">
                {t('uvc.manualModeSoon')}
              </p>
              <div className="flex gap-4 justify-center">
                <button
                  onClick={() => setCurrentStep(Math.max(0, currentStep - 1))}
                  className="px-6 py-2 rounded-xl border border-white/10 hover:bg-white/5 transition-colors"
                >
                  {t('uvc.back')}
                </button>
                <button
                  onClick={() => setCurrentStep(Math.min(STEPS.length - 1, currentStep + 1))}
                  className="px-6 py-2 rounded-xl bg-[#F5C76A] text-black font-semibold hover:shadow-[0_0_20px_rgba(245,199,106,0.4)] transition-all"
                >
                  {t('uvc.nextBtn')}
                </button>
              </div>
            </div>
          )}
        </motion.div>
      </AnimatePresence>
    </div>
  );
}
